package com.xockets.beans;

public class ApplianceDetail {
	
	private String applianceId;
	private String applianceAddress;
	private String applianceState;
	private String applianceCores;
	private String applianceMemory;
	
	public ApplianceDetail() {
		
	}
	
	public String getApplianceId() {
		return applianceId;
	}
	public void setApplianceId(String applianceId) {
		this.applianceId = applianceId;
	}
	public String getApplianceAddress() {
		return applianceAddress;
	}
	public void setApplianceAddress(String applianceAddress) {
		this.applianceAddress = applianceAddress;
	}
	public String getApplianceState() {
		return applianceState;
	}
	public void setApplianceState(String applianceState) {
		this.applianceState = applianceState;
	}
	public String getApplianceCores() {
		return applianceCores;
	}
	public void setApplianceCores(String applianceCores) {
		this.applianceCores = applianceCores;
	}
	public String getApplianceMemory() {
		return applianceMemory;
	}
	public void setApplianceMemory(String applianceMemory) {
		this.applianceMemory = applianceMemory;
	}
	
	

}
